  def toRule( line : String ) : Rule = {
    
    val p1 = line.split("=>")
    
    val pat = p1(0).trim()
    val out = p1(1).trim()
    
    val sp = p1(0).trim().split("/")
    val sz = sp(0).size
    
    Rule( sz, pat, out ) 
    
  }
  
  def hFlip( in : List[String] ) : List[String] = {
    
    val out = ListBuffer[String]()
    
    for( s <- in ){
      out += s.reverse
    }
    
    out.toList
    
  }
  
  def rotate( in : List[String] ) : List[String] = {
    
    val out = ListBuffer[String]()
    
    // get the size
    val sz = in(0).size
    
    for( x <- 0 until sz ){
      var s = ""
      for( y <- sz - 1 to 0 by -1 ){
        s = s + in(y)(x)
        
      }
      out += s
    }
    
    out.toList
    
  }
  
